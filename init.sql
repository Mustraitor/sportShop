-- 收货地址
DROP TABLE IF EXISTS address;
CREATE TABLE address (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  user_id BIGINT NOT NULL,
  name VARCHAR(50),
  phone VARCHAR(20),
  province VARCHAR(50),
  city VARCHAR(50),
  district VARCHAR(50),
  detail VARCHAR(255),
  is_default TINYINT DEFAULT 0,
  is_deleted TINYINT(1) NOT NULL DEFAULT 0 COMMENT '逻辑删除标志：0未删，1已删',
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (user_id) REFERENCES sys_user(user_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 分类
DROP TABLE IF EXISTS category;
CREATE TABLE category (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  name VARCHAR(50),
  parent_id BIGINT DEFAULT 0,
  is_deleted TINYINT DEFAULT 0,
  sort INT DEFAULT 0,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 商品
DROP TABLE IF EXISTS product;
CREATE TABLE product (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  category_id BIGINT,
  name VARCHAR(100),
  description TEXT,
  main_image VARCHAR(255),
  price DECIMAL(10,2),
  status TINYINT DEFAULT 1,
  is_deleted TINYINT DEFAULT 0,
  stock INT DEFAULT 0,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (category_id) REFERENCES category(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- SKU
DROP TABLE IF EXISTS product_sku;
CREATE TABLE product_sku (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  product_id BIGINT,
  sku_name VARCHAR(100),
  price DECIMAL(10,2),
  stock INT DEFAULT 0,
  sku_code VARCHAR(50),
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  is_deleted TINYINT(1) NOT NULL DEFAULT 0 COMMENT '逻辑删除标志：0未删，1已删',
  FOREIGN KEY (product_id) REFERENCES product(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 商品图片
DROP TABLE IF EXISTS product_image;
CREATE TABLE product_image (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  product_id BIGINT,
  url VARCHAR(255),
  sort INT DEFAULT 0,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (product_id) REFERENCES product(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 购物车
DROP TABLE IF EXISTS cart;
CREATE TABLE cart (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  user_id BIGINT,
  product_id BIGINT,
  sku_id BIGINT,
  quantity INT DEFAULT 1,
  checked TINYINT DEFAULT 1,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (user_id) REFERENCES sys_user(user_id),
  FOREIGN KEY (product_id) REFERENCES product(id),
  FOREIGN KEY (sku_id) REFERENCES product_sku(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 订单
DROP TABLE IF EXISTS orders;
CREATE TABLE orders (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  user_id BIGINT,
  address_id BIGINT COMMENT '来源地址ID',
  receiver_name VARCHAR(50) DEFAULT NULL COMMENT '收货人姓名快照',
  receiver_phone VARCHAR(20) DEFAULT NULL COMMENT '收货人手机号快照',
  receiver_province VARCHAR(50) DEFAULT NULL COMMENT '省份快照',
  receiver_city VARCHAR(50) DEFAULT NULL COMMENT '城市快照',
  receiver_district VARCHAR(50) DEFAULT NULL COMMENT '区县快照',
  receiver_detail VARCHAR(255) DEFAULT NULL COMMENT '详细地址快照',
  total_amount DECIMAL(10,2),
  pay_amount DECIMAL(10,2),
  status TINYINT DEFAULT 0,
  -- payment_type TINYINT,
  is_deleted TINYINT DEFAULT 0,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (user_id) REFERENCES sys_user(user_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 订单详情（快照设计）
DROP TABLE IF EXISTS order_item;
CREATE TABLE order_item (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  order_id BIGINT,
  product_id BIGINT,
  sku_id BIGINT,
  product_name VARCHAR(100),
  sku_name VARCHAR(100),
  main_image VARCHAR(512) DEFAULT NULL COMMENT '商品图片快照地址',
  price DECIMAL(10,2),
  quantity INT,
  total_price DECIMAL(10,2),
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (order_id) REFERENCES orders(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 支付
DROP TABLE IF EXISTS payment;
CREATE TABLE payment (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  order_id BIGINT,
  user_id BIGINT,
  amount DECIMAL(10,2),
  payment_method TINYINT,
  trade_no VARCHAR(100),
  status TINYINT,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (order_id) REFERENCES orders(id),
  FOREIGN KEY (user_id) REFERENCES sys_user(user_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 评价
DROP TABLE IF EXISTS review;
CREATE TABLE review (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  user_id BIGINT,
  product_id BIGINT,
  rating INT,
  content TEXT,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (user_id) REFERENCES sys_user(user_id),
  FOREIGN KEY (product_id) REFERENCES product(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 物流
DROP TABLE IF EXISTS order_shipping;
CREATE TABLE order_shipping (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  order_id BIGINT,
  courier_company VARCHAR(50),
  tracking_no VARCHAR(100),
  status VARCHAR(20),
  shipped_at DATETIME,
  delivered_at DATETIME,
  FOREIGN KEY (order_id) REFERENCES orders(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- 秒杀/活动
DROP TABLE IF EXISTS promotion;
CREATE TABLE promotion (
  id BIGINT PRIMARY KEY AUTO_INCREMENT,
  product_id BIGINT,
  sku_id BIGINT,
  promo_price DECIMAL(10,2),
  stock INT,
  start_time DATETIME,
  end_time DATETIME,
  FOREIGN KEY (product_id) REFERENCES product(id),
  FOREIGN KEY (sku_id) REFERENCES product_sku(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

